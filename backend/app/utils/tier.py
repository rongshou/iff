def classify_school_tier(undergrad_school: str | None) -> tuple[int, str]:
    if not undergrad_school:
        return 4, "双非"

    name = undergrad_school.strip()

    # 明确的层次标签
    if name in ("985", "C9"):
        return 1, name
    if name in ("211",):
        return 2, "211"
    if name in ("一本",):
        return 3, "一本"
    if name in ("双非",):
        return 4, "双非"
    if name in ("海本", "英本", "美本", "加本", "澳本", "日韩本", "欧陆本"):
        return 1, "海本"

    # 中外合办
    if name in SINO_FOREIGN_UNIVERSITIES:
        return 1, "海本"

    # 中外合办 → Tier 1 (海本级别)
    if name in SINO_FOREIGN_UNIVERSITIES:
        return 1, "海本"

    # C9（精确匹配，避免"浙江大学" → "浙江大学宁波理工学院" 等误判）
    for c9 in C9_SCHOOLS:
        if name == c9:
            return 1, "C9"

    # 985（精确匹配）
    for kw_985 in TIER_985_KEYWORDS:
        if name == kw_985:
            return 1, "985"

    # 双非一本（优先于generic 211，因为很多一本大学名字含"大学"但不是211）
    if name in TIER_YIBEN_KEYWORDS:
        return 3, "一本"

    # 排除独立学院/分校/分部（如"浙江大学宁波理工学院"、"北京师范大学珠海分校"、"北京大学医学部"）
    if re.search(r"大学.+学院$", name) or re.search(r"大学.+分校$", name) or re.search(r"大学.*(医学|工学|理学|法学|文学|商学|管理|研究|继续教育)部$", name):
        return 4, "双非"

    # 211（含 "大学" 且不在以上名单的）
    if "大学" in name:
        return 2, "211"

    # 其余学院/专科默认双非
    return 4, "双非"


def get_tier_label(tier: int) -> str:
    return SCHOOL_TIERS.get(tier, "双非")
