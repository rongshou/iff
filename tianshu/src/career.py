"""
天枢 · 生涯发展全路径规划模块
基于已确定的"核心主题 + 三级专业推荐",生成 4 阶段生涯路径。
"""

def generate_career_path(student_info: dict, cross_result: dict, major_recommend: dict) -> dict:
    """
    输入:学生基础信息 + 交叉验证结果 + 专业推荐
    输出:4 阶段生涯路径 + 关键节点行动指引(按当前学段过滤)
    """
    current_grade = student_info.get("当前学段", student_info.get("学段", "高一"))
    primary_major = major_recommend["第一优先级(核心适配)"][0]["专业"] if major_recommend["第一优先级(核心适配)"] else "未确定"
    primary_theme = cross_result["核心定位标签"]["主题"]
    mbti_short = cross_result["核心竞争力组合"][1]  # 性格层

    # ── 判断当前教育阶段 ──
    _grade_lower = current_grade.lower()
    if any(k in _grade_lower for k in ("初中", "高一", "高二", "高三", "高中", "中学")):
        edu_level = "high_school"
    elif any(k in _grade_lower for k in ("研", "硕士", "博士", "研究生")):
        edu_level = "graduate"
    elif any(k in _grade_lower for k in ("大一", "大二", "大三", "大四", "大五", "大学", "本科")):
        edu_level = "university"
    elif any(k in _grade_lower for k in ("毕业", "工作", "职场", "已就业")):
        edu_level = "working"
    else:
        edu_level = "university"  # 默认大学

    # 阶段 1:学业深耕期(在校阶段)
    stage1 = {
        "阶段": "学业深耕期(在校阶段)",
        "核心目标": f"夯实{primary_major}相关基础能力,完成学业目标,为升学/求职做好充分准备",
        "关键行动项": [
            f"课程学习:重点学习与{primary_major}相关的基础课程,成绩目标设定在年级前 20%",
            f"实践项目:参与至少 1 个与{primary_major}相关的科研项目/学科竞赛/开源贡献",
            "阅读积累:每年精读 10-15 本专业相关书籍 + 行业研究报告",
            "升学/求职准备:提前 1-2 年了解目标院校/雇主的核心要求",
        ],
        "能力提升重点": [
            f"强化 {primary_theme} 主题相关的基础能力",
            f"基于{mbti_short.split('、')[0]}特质,刻意练习薄弱环节(如沟通、协作)",
        ],
        "资源积累": [
            "寻找 1-2 位领域内的导师或前辈,定期请教",
            "建立专业知识库(笔记、代码、文档)",
        ],
    }

    # 阶段 2:职场起步期(毕业 0-5 年)
    stage2 = {
        "阶段": "职场起步期(毕业 0-5 年)",
        "核心目标": f"进入与{primary_major}相关的适配行业,完成学生到职场人转型",
        "岗位选择建议": [
            f"在{primary_major}方向选择头部公司的初级岗位(校招为主)",
            f"关注岗位的成长性 > 短期薪资,优先选择核心业务部门",
        ],
        "行业适配方向": [
            "结合 MBTI + 霍兰德,选择与核心特质匹配的行业赛道",
            "研究行业头部 10 家企业的招聘要求,锁定 3-5 家重点求职",
        ],
        "核心成长重点": [
            "专业能力:成为岗位内的核心骨干,可独立负责中型项目",
            "职场能力:沟通、协作、项目管理等通用能力快速提升",
            "行业认知:对所处行业的趋势、玩法、关键资源建立系统认知",
        ],
        "职业转型节点": [
            "2-3 年:第一次岗位调整(纵向深耕或横向扩展)",
            "5 年:进入团队 leader 或专家序列的分水岭",
        ],
    }

    # 阶段 3:职场上升期(毕业 5-15 年)
    stage3 = {
        "阶段": "职场上升期(毕业 5-15 年)",
        "核心目标": "实现职业角色升级,从执行者转型为决策者/领域专家",
        "职业角色转型方向": [
            {
                "路径": "技术专家路径",
                "说明": "从骨干工程师 → 高级工程师 → 领域专家 / 首席工程师 / 首席科学家",
                "适合": "深度钻研型、追求技术壁垒",
            },
            {
                "路径": "管理路径",
                "说明": "从团队负责人 → 部门管理者 → 公司高管",
                "适合": "强组织能力、善于协调团队",
            },
            {
                "路径": "创业 / 自由职业路径",
                "说明": "基于核心优势,开启创业、独立咨询、自媒体/内容创作",
                "适合": "高自主性、有独特洞察、抗风险能力强",
            },
        ],
        "核心竞争力打造": [
            "建立不可替代的差异化优势(技术深度 / 行业资源 / 个人品牌)",
            "在所选方向上做出标志性成果(产品 / 论文 / 行业标准)",
        ],
        "资源与影响力积累": [
            "建立行业人脉网络,定期参与行业会议",
            "通过写作、演讲、开源等方式建立个人品牌",
        ],
    }

    # 阶段 4:职业稳定 / 突破期(毕业 15 年以上)
    stage4 = {
        "阶段": "职业稳定 / 突破期(毕业 15 年以上)",
        "核心目标": "实现职业价值的长期沉淀,完成终极目标 + 工作生活平衡",
        "职业发展可选方向": [
            {
                "方向": "行业顶层角色",
                "说明": "成为行业内头部专家、企业核心决策者、行业协会核心成员,参与行业标准制定",
            },
            {
                "方向": "价值沉淀方向",
                "说明": "通过写作、教学、公益、导师角色沉淀个人专业价值",
            },
            {
                "方向": "转型与二次发展",
                "说明": "基于核心优势开启二次创业、跨界发展",
            },
        ],
        "工作生活平衡": [
            "明确工作与生活的边界,避免长期过度消耗",
            "建立可持续的生活节奏与健康管理机制",
        ],
    }

    # 核心职业方向细分适配(对应第一优先级专业)
    career_details = []
    for major_info in major_recommend["第一优先级(核心适配)"]:
        career_details.append({
            "对应专业": major_info["专业"],
            "职业发展路径": f"初级岗位 → 中级骨干 → 高级专家 / 管理者 → 行业领军",
            "核心岗位示例": _get_job_examples(major_info["专业"]),
            "行业头部雇主": _get_top_employers(major_info["专业"]),
        })

    # 关键节点行动指引 — 按当前学段过滤，只显示尚未经历的节点
    _all_nodes = [
        {
            "节点": "高考志愿填报",
            "时间": "高三下学期(截止前 1-3 月)",
            "核心行动": "1. 完成测评最终验证 2. 确定专业分级推荐 3. 完成院校梯队筛选 4. 制定志愿填报方案",
            "注意": "优先保障专业适配度,非盲目追求院校排名;预留保底选项",
            "visible_if": ["high_school"],
        },
        {
            "节点": "考研 / 留学申请",
            "时间": "申请截止前 6-12 个月",
            "核心行动": "1. 确定目标院校与项目 2. 备考/申请分阶段计划 3. 完成科研/实习背景提升 4. 文书与面试准备",
            "注意": "针对目标项目核心录取偏好,重点突出核心特质与适配度",
            "visible_if": ["high_school", "university"],
        },
        {
            "节点": "留学 / 学术深造",
            "时间": "申请截止前 6-12 个月",
            "核心行动": "1. 确定目标院校与导师 2. 准备研究计划/论文发表 3. 强化科研与学术背景 4. 联系推荐人与套磁",
            "注意": "博士申请核心看研究匹配度与学术成果,提前 1-2 年准备",
            "visible_if": ["graduate"],
        },
        {
            "节点": "实习 / 科研项目",
            "时间": "开始前 3-6 个月",
            "核心行动": "1. 确定匹配方向的实习/项目目标 2. 申请与参与的分阶段计划 3. 高质量完成并沉淀成果",
            "注意": "优先选择匹配核心发展方向的项目,非盲目追求大厂/title",
            "visible_if": ["high_school", "university", "graduate"],
        },
        {
            "节点": "毕业 / 职业选择",
            "时间": "毕业前 6-12 个月",
            "核心行动": "1. 确定职业目标赛道 2. 求职分阶段计划 3. 简历优化 + 面试准备 4. offer 筛选与谈判",
            "注意": "选择与核心特质匹配的岗位,非盲目追求高薪",
            "visible_if": ["high_school", "university", "graduate"],
        },
        {
            "节点": "职业转型与提升",
            "时间": "每 2-3 年做一次复盘",
            "核心行动": "1. 评估当前岗位与核心特质的匹配度 2. 了解行业新趋势与机会 3. 规划下一阶段技能提升与角色升级路径",
            "注意": "避免被短期利益驱动频繁跳槽;深耕核心方向,建立不可替代性",
            "visible_if": ["working"],
        },
    ]
    key_nodes = [{k: v for k, v in n.items() if k != "visible_if"} for n in _all_nodes if edu_level in n["visible_if"]]

    # ── 构建阶段列表：已工作者跳过"在校阶段" ──
    all_stages = [stage1, stage2, stage3, stage4]
    if edu_level == "working":
        # 已毕业 → 学业深耕期已过，从职场起步期开始
        stages = all_stages[1:]  # stage2, stage3, stage4
    else:
        stages = all_stages

    return {
        "4阶段路径": stages,
        "核心职业方向细分": career_details,
        "关键节点行动指引": key_nodes,
        "健康与状态管理": [
            "基于 MBTI 和五行特质,设计适配的运动、作息、情绪管理方案",
            "建立常态化的身心状态监测机制",
            "遇到高压场景时,启用预设的压力应对预案",
        ],
    }


