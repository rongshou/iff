def recommend_majors(cross_result: dict, bazi: dict, mbti: dict, holland: dict) -> dict:
    """
    基于交叉验证 + 各体系输入,生成三级专业推荐。
    简化算法:对每个专业,计算与学生特征的总匹配分。
    """
    # 学生特征标签(汇总)
    student_tags = []
    student_tags.append(bazi["日主五行"])  # 五行
    student_tags.append(bazi["最缺五行"])
    student_tags.extend(bazi["喜用神"])
    student_tags.append(bazi["学业事业适配"].split("、"))
    student_tags.append(mbti["基础类型"])
    student_tags.append(mbti["适配专业"])
    student_tags.extend(holland["核心3位代码"])

    # 紫微适配方向
    for gong in ["命宫", "事业宫", "财帛宫"]:
        if gong in cross_result.get("差异点", []):
            pass  # 已在主结果里

    # 计算每个专业的匹配分
    scored = []
    for major, info in MAJOR_LIBRARY.items():
        score = 0
        matched_tags = []
        # 与学生标签匹配
        for tag in student_tags:
            for keyword in info["匹配特征"]:
                if keyword in str(tag):
                    score += 2
                    matched_tags.append(keyword)
        # 跨验证主题加权
        primary_theme = cross_result["核心定位标签"]["主题"]
        theme_to_major = {
            "研究/学术": ["计算机科学与技术", "数学与应用数学", "人工智能", "数据科学与大数据技术", "心理学"],
            "技术/工程": ["计算机科学与技术", "软件工程", "电子信息工程", "人工智能"],
            "逻辑/系统": ["计算机科学与技术", "软件工程", "数据科学与大数据技术"],
            "创造/艺术": ["视觉传达设计", "汉语言文学"],
            "教育/服务": ["教育学", "心理学"],
            "管理/领导": ["金融学", "工商管理"],
            "商业/销售": ["金融学"],
            "动手/实践": ["软件工程", "电子信息工程"],
        }
        if major in theme_to_major.get(primary_theme, []):
            score += 5

        # 去重
        matched_tags = list(set(matched_tags))

        if score > 0:
            scored.append((major, info, score, matched_tags))

    # 按分数排序
    scored.sort(key=lambda x: x[2], reverse=True)

    # 分级
    if len(scored) >= 3:
        first_priority = scored[:2]
        second_priority = scored[2:4] if len(scored) > 2 else []
        third_priority = scored[4:6] if len(scored) > 4 else []
    else:
        first_priority = scored
        second_priority = []
        third_priority = []

    # 风险规避(基于学生短板 + 八字忌神)
    risk_majors = []
    if "社交" in mbti["短板"] or "敏感" in mbti["短板"]:
        risk_majors.append({
            "专业": "市场营销/销售类",
            "风险": "高社交强度岗位与内向型人格冲突",
            "替代": "产品经理、技术型 BD",
        })
    if "保守" in mbti["短板"]:
        risk_majors.append({
            "专业": "创业/风险投资类",
            "风险": "高不确定性赛道与保守型性格冲突",
            "替代": "大型企业战略岗、咨询",
        })

    # 八字忌神相关风险
    ji_shen = bazi["忌神"]
    if "金" in ji_shen:
        risk_majors.append({
            "专业": "金融工程/硬核金融",
            "风险": "金气过旺可能带来压力,需注意身心平衡",
            "替代": "应用金融、保险精算",
        })

    return {
        "第一优先级(核心适配)": [
            {
                "专业": m,
                "细分方向": info["细分方向"],
                "匹配分": score,
                "匹配特征": info["匹配特征"],
                "学生命中关键词": matched,
                "核心课程": info["核心课程"],
                "核心能力要求": info["核心能力"],
                "院校梯队": info["院校梯队"],
                "匹配逻辑": f"与学生「{cross_result['核心定位标签']['主题']}」主题高度契合 + 与{mbti['基础类型']}+{holland['核心3位代码']}特征匹配",
            }
            for m, info, score, matched in first_priority
        ],
        "第二优先级(次优适配)": [
            {
                "专业": m,
                "细分方向": info["细分方向"],
                "匹配分": score,
                "匹配特征": info["匹配特征"],
                "核心课程": info["核心课程"],
                "院校梯队": info["院校梯队"],
                "匹配逻辑": f"匹配学生{mbti['基础类型']}优势 + 可作为备选长期发展",
            }
            for m, info, score, matched in second_priority
        ],
        "第三优先级(潜力适配)": [
            {
                "专业": m,
                "细分方向": info["细分方向"],
                "匹配分": score,
                "匹配特征": info["匹配特征"],
                "匹配逻辑": "具备一定匹配度,可作为补充选择或交叉学科方向",
            }
            for m, info, score, matched in third_priority
        ],
        "风险规避清单": risk_majors if risk_majors else [
            {
                "专业": "(无特别高风险专业)",
                "风险": "当前测评结果未发现明显冲突专业",
                "替代": "持续动态评估",
            }
        ],
    }


