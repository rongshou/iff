def get_mbti_info(mbti_type: str) -> dict:
    """输入 MBTI 4 字母 + A/T(如 INTJ-A),返回核心解析。"""
    # 拆分类型(可能带 -A/-T)
    base_type = mbti_type.upper().split("-")[0]
    if base_type not in MBTI_DATA:
        return {"错误": f"未识别的 MBTI 类型:{mbti_type},请检查输入(如 INTJ、ESFP-A、INFP-T)"}

    data = MBTI_DATA[base_type].copy()
    data["完整类型"] = mbti_type.upper()
    data["基础类型"] = base_type

    # A/T 倾向补充
    at_suffix = mbti_type.upper().split("-")[-1] if "-" in mbti_type.upper() else ""
    if at_suffix == "A":
        data["倾向"] = "Assertive(自信型):更稳定、更少自我怀疑"
    elif at_suffix == "T":
        data["倾向"] = "Turbulent(动荡型):更敏感、追求进步"
    else:
        data["倾向"] = "未提供 A/T 倾向"

    return data


